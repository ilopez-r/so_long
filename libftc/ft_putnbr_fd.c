#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = n * -1;
		}
		if (n <= 9)
		{
			c = (n % 10) + 48;
			write (fd, &c, 1);
		}
		if (n > 9)
		{
			c = (n % 10) + 48;
			n = n / 10;
			ft_putnbr_fd(n, fd);
			write(fd, &c, 1);
		}
	}
}
